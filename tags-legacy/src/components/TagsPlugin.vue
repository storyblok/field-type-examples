<template>
  <div class="tags-container">
    <vue-tags-input
      :value="inputValue"
      :tags="tags"
      :placeholder="options.placeholder || ''"
      @input="handleInputValueChange"
      @tags-changed="handleValueChange"
    >
      <div slot="tag-actions" slot-scope="props">
        <i @click="props.performDelete">
          <svg
            width="8"
            height="8"
            viewBox="0 0 8 8"
            fill="none"
            xmlns="http://www.w3.org/2000/svg"
          >
            <rect
              x="0.0857849"
              y="1.5"
              width="2"
              height="9"
              rx="1"
              transform="rotate(-45 0.0857849 1.5)"
              fill="#1B243F"
            />
            <rect
              x="6.44975"
              y="0.0858307"
              width="2"
              height="9"
              rx="1"
              transform="rotate(45 6.44975 0.0858307)"
              fill="#1B243F"
            />
          </svg>
        </i>
      </div>
    </vue-tags-input>
  </div>
</template>
<script>
import VueTagsInput from '@johmun/vue-tags-input'

export default {
  components: {
    VueTagsInput,
  },
  props: {
    options: {
      type: Object,
      default: undefined,
    },
    setValue: {
      type: Function,
    },
    value: {
      type: Object,
    },
  },
  data() {
    return {
      inputValue: '',
    }
  },
  computed: {
    tags() {
      return this.value.value?.map((text) => ({ text }))
    },
  },
  methods: {
    handleInputValueChange(inputValue) {
      this.inputValue = inputValue
    },
    handleValueChange(tags) {
      this.setValue({ value: tags.map((tag) => tag.text) })
    },
  },
}
</script>

